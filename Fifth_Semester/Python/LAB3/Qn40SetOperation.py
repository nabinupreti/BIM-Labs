# TDemonstrate set operations like union, intersection, difference, and symmetric difference
s1 = {1, 2, 3}
s2 = {3, 4, 5}

# Union
print("Union:", s1 | s2)

# Intersection
print("Intersection:", s1 & s2)

# Difference
print("Difference (s1 - s2):", s1 - s2)

# Symmetric difference
print("Symmetric difference:", s1 ^ s2)
