fs1 = frozenset({1, 2, 3})
fs2 = frozenset({3, 4, 5})

# Union
print("Union of frozensets:", fs1 | fs2)

# Intersection
print("Intersection of frozensets:", fs1 & fs2)

# Difference
print("Difference of frozensets (fs1 - fs2):", fs1 - fs2)

# Symmetric difference
print("Symmetric difference of frozensets:", fs1 ^ fs2)
