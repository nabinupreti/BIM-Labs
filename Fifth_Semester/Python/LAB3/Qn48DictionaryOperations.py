#Demonstrate various dictionary operations like accessing items, adding and removing items, iterating through keys and values
d = {"a": 1, "b": 2, "c":3}
print("Original dictionary: ", d)

#accessing items
print("Accessing item: ", d["a"])

#adding item
d["d"] = 4
print("After adding key 'd':", d)

#removing item
del d["a"]

