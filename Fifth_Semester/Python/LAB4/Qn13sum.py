def add(a, b):
    """
    This function takes two numbers as input and returns their sum.
    Parameters
    ----------
    a : int
        First number to add.
    b : int
        Second number to add.

    Returns
    -------
    int
        The sum of ``a`` and ``b``.
        
    Examples
    --------
    >>> add(2, 2)
    4
    """
    return a + b
