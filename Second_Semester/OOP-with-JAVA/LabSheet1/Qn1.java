/*Write a program in java to describe how primitive data types (integer, float, double, 
characters and Boolean) are declared and display the result */
public class Qn1 {
    public static void main(String[] args) {
        int n1 = 120;
        Double n2 = 20.3;
        float n3 = 30.3f;
        boolean b = true;
        char ch = 'n';
        System.out.println("integer is " + n1);
        System.out.println("float is " + n2);
        System.out.println("Double is " + n3);
        System.out.println("Boolean is " + b);
        System.out.println("Character is " + ch);

    }
}
