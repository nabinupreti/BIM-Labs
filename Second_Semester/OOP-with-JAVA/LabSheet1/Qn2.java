/*Write a program to describe different literals (positive integer literal, negative integer 
literals, hexadecimal representation, octal, binary etc)
 */
public class Qn2 {
    public static void main(String[] args) {
        int p = 90;
        int n = -59;
        int o = 067;
        int h = 0x9AB;
        int b = 0b0110;

        System.out.println("Positive intiger is " + p);
        System.out.println("Negative intiger is " + n);
        System.out.println("Octal is " + o);
        System.out.println("Hexadecimal is " + h);
        System.out.println("Binary is " + b);
    }
}
